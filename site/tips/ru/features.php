<h3>Тема</h3>
Для отображения контента в проекции программа использует темы для применения форматирования отображения.<br>
Можно создать несколько тем для использования в различных функциях программы и легко переключаться между созданными темами одним щелчком мыши.<br>
Доступные настройки темы:<br>
<ul>
  <li>Фон: сплошной цвет, изображение, видео, прозрачный.</li>
  <li>Шрифт, размер, цвет, контур, яркость, тень, жирный шрифт, курсив.</li>
  <li>Поля, вертикальное и горизонтальное выравнивание, расстояние между строками и символами.</li>
  <li>Цвет фона и контур: примените цвет фона к каждой строке.</li>
  <li>
Текстуры — можно использовать текстуры в качестве «цвета» шрифта и цвета фона (<a href='/tips/color_texture.html' target='_blank'>Подробнее</a>)<br>
• Сплошной цвет: используйте стандартный сплошной цвет<br>
• Градиент: используйте комбинацию двух или более цветов, образуя горизонтальный или вертикальный градиент<br>
• Изображение: используйте изображение в качестве текстуры для рендеринга<br>
• Прозрачный: прозрачное отображение шрифта в сочетании с цветом фона, создавая эффект пустоты и показывая фон позади текста.</li>
  <li>Комментарий: настройки форматирования комментариев.</li>
  <li>Различные настройки: отображение текста заглавными буквами, установка автоматического переноса строк.</li>
  <li>Упорядочивайте темы по тегам, например: синий, вода, круг и т. д.</li>
</ul>
<b>Примечание.</b> По умолчанию видео, используемые в качестве фона, воспроизводятся только на общедоступном экране из соображений производительности.<br>
На других экранах системы вместо фонового видео будет использоваться статическое изображение.<br>
В меню «Файл», настройках, вкладке «Дополнительно» можно установить дополнительный экран для отображения видео в качестве фона.

