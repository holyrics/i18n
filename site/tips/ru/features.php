<h3>Масштаб</h3>
Наряду с регистрацией служб и мероприятий в программе Holyrics можно регистрировать команды, роли и участников, что позволяет создавать расписания служб и событий.<br>
Основное внимание в программе уделяется не администрированию и организации команд, программа предоставляет только базовые функции администрирования и создания расписания.

