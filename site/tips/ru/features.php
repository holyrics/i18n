<h3>Изображения, аудио, видео и файлы</h3>
Программа Holyrics интегрирована с программой VLC Player для воспроизведения аудио и видео.<br>

<ul>
  <li>Библиотека аудио, видео и изображений в главном окне программы для организации файлов.</li>
  <li>Поиск файлов в папке (и подпапках, в зависимости от конфигурации)</li>
  <li>
Когда вы создаете подпапку, она становится списком воспроизведения для отображения медиафайлов в этой подпапке.<br>
Другими словами, чтобы отобразить последовательность изображений, создайте подпапку и добавьте все изображения в соответствующую подпапку.<br></li>
  <li>Автоматическое отображение изображений (правая кнопка мыши на подпапке)</li>
  <li>Поддерживает перетаскивание файлов.</li>
  <li>Добавьте ярлыки для отображения файлов из других папок на вашем компьютере.</li>
  <li>Установить размер для отображения видео и изображений: заполнить, подогнать...</li>
  <li>Создать видео из последовательности изображений (щелкните правой кнопкой мыши подпапку с изображениями)</li>
  <li>Применить к изображению эффекты (яркость, контрастность, оттенок и т. д.)</li>
  <li>Отображение изображения с эффектом размытия, когда изображение выходит за пределы проекционного экрана.</li>
  <li>Отображение разных изображений (например, разного разрешения) на разных экранах (<a href='/tips/image_multiple_screens.html' target='_blank'>Подробнее</a>)</li>
  <li>
Редактирование медиа (щелкните правой кнопкой мыши по медиа - редактировать)<br>
Аудио – вырезанный отрывок<br>
Видео - вырезать раздел/изменить разрешение и битрейт/извлечь аудио<br>
Изображение – обрезка/изменение размера</li>
  <li>
Возможность переименовать файл (правая кнопка мыши) непосредственно из программы вместо переименования из проводника операционной системы<br>
Это позволяет обновлять ссылки на файлы, используемые в функциях программы (например, панель закладок), при переименовании файла.<br>
При переименовании через файловый менеджер теряются ссылки на ресурсы программы, пытаются получить доступ и выполнить файл с прежним именем.</li>
  <li>
На вкладке «Файлы» вы можете добавлять и упорядочивать файлы любого типа, но файлы, как правило, будут открываться самой операционной системой.<br>
Программа Holyrics может запускать файлы .ppt .pptx из этой папки, но она доступна только для Windows, и для этого необходимо установить программу PowerPoint.</li>
</ul>

