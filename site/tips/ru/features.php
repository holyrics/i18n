<h3>Масштаб</h3>
Наряду с регистрацией служб и мероприятий в программе Holyrics можно регистрировать команды, роли и участников, что позволяет создавать расписания служб и событий.<br>
Основное внимание в программе уделяется не администрированию и организации команд, программа предоставляет только базовые функции администрирования и создания расписания.<br>
<b>Создание списков на основе участников:</b> сетка, в которой каждый столбец представляет собой служение или событие, каждая строка — это член, а каждая ячейка — поле, позволяющее определить, запланирован ли участник на участие в соответствующем служении/мероприятии. <br>
В этом типе расписания невозможно определить, какую роль будет выполнять участник.<br>
<b>Создавайте списки на основе ролей:</b> сетка, в которой каждый столбец — это служение или событие, каждая строка — это роль, а каждая ячейка — список участников, которым можно назначить ту или иную роль.<br>

