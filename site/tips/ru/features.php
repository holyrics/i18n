<h2>Список основных функций программы Holyrics</h2>

<h3>Проекция</h3>
Программа Holyrics — это программное обеспечение, позволяющее отображать и управлять презентациями на дополнительных 
мониторах системы, подключенных непосредствеенно к видеокарте ПК, а также на других устройствах в локальной сети для 
получения дополнительной информации.<br>
Поддерживаются:
<ul>
  <li>Песни</li>
  <li>Библейские стихи</li>
  <li>Произвольные тексты</li>
  <li>Изображения, аудио и видео</li>
  <li>Обратный отсчет</li>
  <li>Предупреждения бегущей строкой (внизу или вверху проекционного экрана)</li>
  <li>Текст на мониторе сцены</li>
  <li>Обратный отсчет или таймер на мониторе сцены</li>
  <li>Оповещения на мониторе сцены</li>
</ul>

<hr />

<h3>Форматирование слайдов на основе тем</h3>
Holyrics работает таким образом, что любой текст (Песни, стихи Библии и пр.), отображаемый в проекции на экране, 
форматируется в режиме реального времени в соответствии с заранее настроенной темой оформления. 
Тема имеет широкие настройки шрифта, цвета, фона, выравнивания и т. п.
Другими словами, нет необходимости вручную применять форматирование к каждому слайду, как это требуется в программах типа PowerPoint, 
достаточно выбрать желаемую тему и текст.<br>

