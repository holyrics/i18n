  <li>Возможность разделить длинные стихи для отображения на нескольких слайдах.</li>
  <li>Отображать только ссылку на стих без текста.</li>
  <li>Создавайте ярлыки для конкретного перевода Библии, например, связывая имя человека с переводом.</li>
  <li>Кнопка для сравнения версий и отображения текущего стиха в различных доступных переводах Библии.</li>
