<h3>Служения и мероприятия</h3>
<ul>
  <li>Регистрация регулярных служений и событий в системе.</li>
  <li>Зарегистрируйте конкретное событие, указав его дату и время.</li>
  <li>
Зарегистрированные сервисы автоматически создают плейлисты для каждого сервиса.<br>
Например, для службы, зарегистрированной как «каждое воскресенье в 19:00», плейлист будет автоматически создаваться каждое воскресенье в 19:00.<br>
Позволяет вам регистрировать музыку и медиафайлы, используемые в каждом богослужении и мероприятии независимо.</li>
  <li>
Настройте <a href='#wallpaper'>обои</a> для служения (файл меню, настройки, обои)<br>
Установите <a href='#wallpaper'>обои</a> для мероприятия (во всплывающем окне редактирования мероприятия)<br>
Таким образом, <a href='#wallpaper'>обои</a>, используемые в проекции, автоматически будут изображением, определенным для соответствующего служения или события из списка воспроизведения, выбранного в главном окне программы.<br>
Избегайте необходимости менять обои для каждой услуги или события.</li>
  <li>
При регистрации услуги с правилом «все» есть возможность скрыть отдельные недели<br>
Например, вы хотите зарегистрировать Святое Причастие «каждое второе воскресенье»<br>
Если у вас есть услуга, зарегистрированная как «каждое воскресенье», может быть создан дубликат плейлиста<br>
Таким образом, скрывая вторую неделю службы «каждое воскресенье», можно избежать дублирования списка, создавая только плейлист службы «Святая Вечеря» для второго воскресенья каждого месяца.</li>
</ul>

