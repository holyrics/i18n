<h3>Плейлист</h3>
Все услуги, регулярные события и уникальные события, зарегистрированные в системе, формируют независимый плейлист, что позволяет заранее организовать плейлист для каждой услуги и события, а также вести историю всех ранее созданных программ.
<ul>
  <li>
Плейлист с текстами песен<br>
Добавьте все тексты песен, которые будут использоваться в соответствующем сервисе или мероприятии.<br>
Можно выбрать несколько элементов для последовательного выполнения<br>
Опция в настройках для автоматического сохранения истории просмотра (отметить как прослушанную) при просмотре текстов песен и их включения в список воспроизведения.</li>
  <li>
Медиа-плейлист (также поддерживает тексты песен)<br>
Медиа в данном случае — это общий термин, он не означает просто аудио, видео и изображение<br>
Медиа-плейлист – это место для добавления и систематизации всех элементов, которые будут воспроизводиться на соответствующей службе или мероприятии.<br>
Тексты песен, библейские стихи, текст, аудио, видео, изображения, объявления, автоматическое представление, обратный отсчет и т. д.<br>
Вы можете организовать список с помощью заголовков, которые могут скрывать или расширять подэлементы, добавленные под соответствующим заголовком (<a href='/tips/playlist_title.html' target='_blank'>Подробнее</a>).<br>
Установите продолжительность для каждого элемента списка<br>
Отобразить вероятное время выполнения элемента (время расписания) от начала услуги/события и продолжительность каждого предыдущего элемента в списке (временная шкала).</li>
  <li>Поделитесь в Интернете списком текстов песен, к которому можно получить доступ через браузер, а также загрузить на другой компьютер.</li>
  <li>Сохранение плейлиста в файл для открытия на другом компьютере.</li>
  <li>Сохранять шаблоны списков мультимедиа для повторного использования в других сервисах и мероприятиях.</li>
</ul>

