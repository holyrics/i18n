<h1>Holyrics - Script</h1>

É possível criar scripts baseados na linguagem de programação JavaScript que executam ações no programa Holyrics.<br />
<br />
Esses scripts podem automatizar tarefas e realizar uma lista de ações de uma vez só.<br />
<br />
É possível criar itens para serem executados com um clique com mouse, adicionando os itens na barra de favoritos, por exemplo.<br />
<br />
E também para execução automática na funcionalidade <b>Gatilhos</b> do programa, onde uma ação é executada automaticamente quando outra ação é feita no programa, como uma mudança de slide, por exemplo.<br />
<br />
Os scripts são criados pelo submenu <b>Script</b> disponível na barra de favoritos e no menu ferramentas, diversos.<br />
Ou utilizando o receptor <b>Holyrics - Script</b> disponível na lista de receptores de um <b>API Item</b> ou da funcionalidade <b>Gatilhos</b>.<br />
<br />
