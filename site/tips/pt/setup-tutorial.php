<h1>Tutorial de instalação Mac</h1>
<br />
<p>
    Extraia os arquivos baixados (<b>Holyrics.zip</b>) para qualquer pasta no computador.<br />
    Abra o arquivo <b>Holyrics.jar</b> para executar o programa.<br />
    Caso dê algum erro na abertura do arquivo, é necessário verificar/instalar o Java 8 no sistema, conforme tutorial abaixo.<br />
    Após a instalação do Java, abra o arquivo <b>Holyrics.jar</b> para executar o programa.<br />
</p>
Para instalar o Java:<br />
<p style='padding-left:32px'>
    Abra o terminal e execute  os seguintes comandos para desinstalar qualquer versão do Java atualmente instalada no computador:<br />
    <code>sudo rm -fr /Library/Internet\ Plug-Ins/JavaAppletPlugin.plugin</code><br />
    <code>sudo rm -fr /Library/PreferencesPanes/JavaControlPanel.prefPane</code><br />
    <code>sudo rm -fr ~/Library/Application\ Support/Java</code><br />
    <br />
    Depois instale o Java 8 diretamente do site <a href='https://java.com/en/download/manual.jsp' target='_blank'>java.com</a><br />
    Ou baixe Java compatível<a href='/download/file/jre-8u211-macosx-x64.dmg'> (Baixar)</a><br />
</p>
