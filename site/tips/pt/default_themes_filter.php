<h1>Filtro padrão de Temas</h1>

Um Tema pode ser facilmente reutilizado em qualquer ambiente do programa, mas em muitas situações um Tema criado pelo usuário tem um uso específico em uma funcionalidade do programa.<br />
Para facilitar a organização é possível definir um filtro padrão para que a lista de temas nas diferentes funcionalidades do programa mostre apenas os itens que você definir para aquela funcionalidade.<br />
<br />
Para fazer isso você precisa organizar os temas por tags e depois definir quais são as tags aceitas em cada funcionalidade.<br />
<br />
Exemplo: Crie uma tag com o nome "Versículos" (menu tema, tags) e adicione todos os temas que você criou para exibir versículos nessa tag.<br />
E então acesse o menu tema, filtro, selecione a aba <b>Bíblia</b> e selecione a tag "Versículos" <br />
<br />
Dessa forma somente os temas marcados com a tag "Versículos" serão listados na janela da Bíblia<br />
<br />
<b>Observação:</b><br />
Caso você ative o filtro, sempre que criar um novo tema lembre-se de adicionar a tag correspondente para que o tema seja listado corretamente. <br />
