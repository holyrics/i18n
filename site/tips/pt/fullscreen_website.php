Para abrir uma página em tela cheia na tela de projeção, crie um item <b>URL</b> na lista de reprodução ou favoritos.<br />
<br />
Ao executar o item, marque a opção <b>Exibir na tela (Projeção)</b> e clique em <b>OK</b>.<br />
<br />
O link será aberto no navegador em tela cheia na tela de projeção.<br />
Será aberta uma pequena janela de controles básicos, que permite executar alguns comandos na página sem ter que arrastar o mouse até o navegador.<br />
<br />
<br />
Obs.: Não é considerado uma apresentação, ou seja, o site será exibido somente na tela público, não será exibido nas telas de retorno.<br />
Está disponível apenas para Windows.<br />
É necessário ter Chrome ou Edge disponível no sistema.<br />
<br />
