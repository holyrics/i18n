<h1>Descrição do Slide</h1>

A funcionalidade <b>Descrição do Slide</b> serve como facilitador para identificação do slide no rodapé de apresentação.<br>
<br />
A descrição de um parágrafo/slide pode ser definida:

<ul>
    <li>
        na janela de edição da letra, após duas ##, dentro de parênteses;<br />
        <img src='images/slide_label_img02.png' />
        <br />&nbsp;
    </li>
    <li>
        na janela de edição da letra, na visualização dos slides;<br />
        <img src='images/slide_label_img03.png' />
        <br />&nbsp;
    </li>
    <li>
        na janela principal, botão direito do mouse.<br />
        <img src='images/slide_label_img01.png' />
        <br />&nbsp;
    </li>
</ul>

<br>
<br>
A descrição pode ser utilizada para:
<ul>  
    <li>definir atalhos na função <b>ir para</b>, ex.: letra <b><i>X</i></b> ir para o slide <b><i>X</i></b>;</li>
    <li>definir um Tema padrão, ex.: todos os slides <b><i>X</i></b> terão o Tema <b><i>X</i></b>;</li>
    <li>formatação personalizada, ex.: todos os slides <b><i>X</i></b> com a fonte itálica</b>.</li>
</ul>
<br>

<img src='images/slide_label_img04.png' />
<br />
<br />
Dessa forma, você pode digitar o atalho e depois <i><b>ENTER</b></i> na caixa de texto "ir para..." no rodapé de apresentação que vai mudar pro slide da música conforme descrição.<br />
