<h1>Apresentação Rápida de Versículos</h1>

Ao exibir um item <b>Texto</b> ou um arquivo <b>.pptx</b>, caso exista uma referência bíblica no texto do slide, o versículo poderá ser exibido de forma rápida sem encerrar a apresentação atual.<br />
<br />
Clique com o botão direito do mouse em cima do slide no rodapé de apresentação e utilize a opção <b>Apresentação Rápida</b>.<br />
<br />
<img src='images/quick_verse_presentation_img01.png' /><br />
<br />
Exemplo de referências: <br />
Gn 1.3<br />
Salmos 23<br />
João 3:16<br />
RM 12:1-2<br />
<br />
O texto do slide pode conter múltiplas referências.<br />
<br />
<b>Observação:</b><br />
O nome do livro e a abreviação que serão identificadas serão conforme a versão principal definida na janela da Bíblia no programa.<br />
Se alguma referência não estiver sendo identificada, confira se o nome do livro no slide está no mesmo idioma da Bíblia padrão na janela da Bíblia.<br />
