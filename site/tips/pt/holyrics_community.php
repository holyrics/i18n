<h1>Holyrics Community</h1>

<p>
    Um item de música no programa pode conter informações sobre a música como tonalidade, BPM, tempo e até links para plataformas de stream.<br />
    Por praticidade, essas informações já são incluídas ao pesquisar uma letra de música no programa pela pesquisa holyrics.com <br />
    Também é possível buscar essas informações para uma música já salva anteriormente, pelo botão de "lupa" na janela de edição de letra.<br />
    <img src='images/song_info_example.png' /><br />
</p>
<p>
    Disponibilizamos um bot no Telegram para que a comunidade Holyrics possa colaborar com essas informações e termos uma base de dados completa e confiável.<br />
    <br />
    Para colaborar, acesse <a href="https://t.me/HolyricsCommunityBot" target='_blank'>@HolyricsCommunityBot</a> e inicie a conversa no chat, seguindo as instruções.
