<h1>Autor da música</h1>

<p>
    De acordo com a lei de direitos autorais, é obrigatória a exibição do nome do <b>autor</b> de uma música ao fazer uso dela.<br />
    Ou seja, ao projetar a letra de uma música no culto é necessário também informar o <b>autor</b> responsável por essa obra.<br />
    Isso pode ser feito antes de iniciar a projeção (slide inicial) ou no rodapé de cada slide durante a apresentação da respectiva letra.
</p>
<p>
    O programa Holyrics sempre teve os campos <b>título</b> e <b>artista</b> na criação/edição de uma música e exibia a informação escrita no campo <b>artista</b> na projeção.<br />
    O usuário que quisesse exibir o nome do autor na projeção, em conformidade com a lei de direitos autorais, precisava alterar o conteúdo do campo <b>artista</b> e escrever o(s) <b>autor(es)</b> nesse campo.<br />
    Isso fazia o usuário perder uma informação importante que é o artista, que facilita em buscas, organização e também identifica a "versão" da música, pois muitas músicas têm versões diferentes para artistas diferentes.<br />
    Além da informação do autor de uma música não ser tão fácil e simples de conseguir.
</p>
<p>
    Para resolver isso, o programa Holyrics, na versão 2.13.0, adicionou o campo <b>autor</b> na janela de edição de música.<br />
    E também disponibilizamos uma funcionalidade bastante útil nesses casos para que você não precise ficar procurando o autor de uma música, que é a opção do programa identificar automaticamente o autor de uma música, com base na letra da música.<br />
    Essa identificação é feita no banco de dados online do servidor Holyrics.<br />
    Já temos milhares de músicas com essa informação disponível e estaremos aumentando sempre que possível.<br />
    <br />
    Procuramos realizar a pesquisa e identificação dos autores com bastante cuidado.<br />
    Porém não podemos garantir que todos os autores estejam identificados corretamente, pois até encartes de LPs e CDs já tiveram informações de autores incorretas, incompletas e/ou conflitantes.<br />
    Podemos somente afirmar que a informação disponibilizada tem um alto grau de confiança.<br />
    As fontes de pesquisa são: Spotify, ECAD, canais oficiais do YouTube, encartes de CDs e LPs, dentre outros.<br />
    <br />
    Você pode conferir os detalhes nas imagens a seguir.<br />
</p>

<p>
    Obs.: A identificação é feita utilizando a letra da música digitada e não o título e artista.
</p>

