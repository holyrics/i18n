<h1>Letra de música em múltiplos idiomas</h1>

Em algumas situações é necessário exibir a letra de uma música em dois (ou mais) idiomas diferentes.<br />
É possível fazer isso de forma prática no programa, mantendo a independência de cada slide, o que facilita a configuração para o usuário.<br />
<br />
<ul>
    <li>
        Na janela de edição de letra, acesse o ícone de <b>3 pontinhos</b>, opção <b>Editor Avançado</b>.<br />
        <img src='images/multiple_languages_img01.jpg' /><br />
        <br />
    </li>
    <li>
        Acesse a opção <b>Tradução</b> (ícone: <img src="data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAABAAAAAQCAYAAAAf8/9hAAAAqklEQVR42mNgoBHQAeISIJ4NxPOBuAYqRhDwQDX8BuL/UPo7lA3Cq4FYBJ/m3UD8HIhjgPgzEPcDcTsQv4eKPQbiw7gMmQ/VrABV/BuqUATqihSo3GOoSzD8DNJwHmrQZait86H4NRBfh7LPQ72DEiYlaBpANp5G4h+HWoBsYA2yAaDQXo/E/w11MgwkQG2FgeVQg6hnAMVeoDgQKY5GqiQkipMyVTITyQAABEZgxYNbhZoAAAAASUVORK5CYII=" />);<br />
        <img src='images/multiple_languages_img02.jpg' /><br />
        <br />
    </li>
    <li>
        Crie uma tradução e preencha os slides correspondentes da letra da música com o texto traduzido.<br />
        <img src='images/multiple_languages_img03.jpg' /><br />
        <br />
    </li>
    <li>
        Confira o resultado na janela de edição e salve a alteração.<br />
        <img src='images/multiple_languages_img04.jpg' /><br />
        <br />
    </li>
</ul>
<br />
Pronto, dessa forma você facilmente adiciona traduções de forma independente para cada parágrafo da letra da música.<br />
E então você pode definir no programa de forma como a letra da música será exibida em cada tela.<br />
Menu arquivo, configurações de exibição.<br />
<img src='images/multiple_languages_img05.jpg' /><br />
<br />
Há várias possibilidades de uso:<br />
Exibir em uma tela somente o slide original e exibir em outra tela somente o slide traduzido.<br />
<br />
Exibir na mesma tela o slide original e traduzido simultaneamente, com o texto separado por uma linha em branco.<br />
<pre>Original a
Original b
 
<i>Traduzido a
Traduzido b</i></pre>
<br />
Exibir na mesma tela o slide original e traduzido simultaneamente, com o texto mesclado.<br />
<pre>Original a
<i>Traduzido a</i>
 
Original b
<i>Traduzido b</i></pre>
<br />
Podendo definir também um estilo/formatação diferente para o texto que está traduzido, adicionando um destaque entre os diferentes textos.<br />
