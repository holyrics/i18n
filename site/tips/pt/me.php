<p>
    Você já conhece o site <b>me.holyrics.com</b>?
</p>
<p>
    Trata-se de uma página de perfil para sua igreja e/ou ministério de louvor, onde é possível visualizar as músicas tocadas na sua igreja de acordo com os dados compartilhados pelo programa através do usuário do menu "compartilhar".
</p>
<p>
    Dessa forma os membros ou integrantes do louvor poderão consultar e verificar as músicas tocadas recentemente na sua igreja e até mesmo pessoas que queiram saber mais sobre a igreja poderão ter uma informação mais detalhada sobre a parte do louvor.
</p>
<p>
    Além dessas informações, o perfil também poderá ter links para as redes sociais da sua igreja/ministério, sendo assim um meio para divulgar todas as suas redes sociais com apenas um link, por exemplo: <a href='https://me.holyrics.com.br/igrejarenovo' target='_blank'>me.holyrics.com/igrejarenovo</a>
</p>
<p>
    Mesmo que você não tenha internet na sua igreja ou não compartilhe os dados do programa online, ainda sim você pode ter um perfil na página para exibir os links para as redes sociais, por exemplo: <a href='https://me.holyrics.com.br/holyrics' target='_blank'>me.holyrics.com/holyrics</a>
</p>
<p>
    Aproveite e faça o cadastro da sua igreja no site: <br />
    <a href='https://me.holyrics.com.br/login/register.html'>me.holyrics.com/login/register.html</a>
</p>

