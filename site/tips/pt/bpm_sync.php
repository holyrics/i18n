<h1>BPM Sync</h1>

Em muitos casos os vídeos de plano de fundo utilizados na projeção são rápidos demais para uma música mais lenta e outros vídeos são lentos demais para uma música mais agitada. <br />
<br />
É possível ativar a sincronização automática no programa para que a velocidade de reprodução do vídeo de plano de fundo na exibição da letra da música seja de acordo com a velocidade da música.<br />
<br />
Para que a sincronização seja feita, a letra da música e o vídeo de plano de fundo precisam ter um valor BPM definido.<br />
<br />
O valor BPM da letra da música pode ser definido na janela de edição de letra, na opção <b>Extra</b> <br />
<img src='images/bpm_sync_img01.png' /><br />
<br />
O valor BPM do vídeo pode ser definido na aba de seleção de plano de fundo.<br />
<img src='images/bpm_sync_img02.png' /><br />
<br />
Sempre que uma letra de música for apresentada, o valor BPM global do programa será atualizado com o valor BPM definido na letra da música. <br />
E se o vídeo de plano de fundo utilizado na exibição dos slides dessa letra de música tiver um valor BPM definido, o vídeo será executado na velocidade relativa. <br />
Por exemplo, se a letra da música estiver definida como BPM=90 e o vídeo estiver definido como BPM=120, então o vídeo será executado na velocidade 0,75.<br />
Ou se a música for BPM=120 e o vídeo BPM=90, o vídeo será executado na velocidade 1,33.<br />
<br />
<b>Para ativar a sincronização:</b><br />
Ative a exibição do item BPM no <b>menu janelas, barra de menu</b>.<br />
Depois clique com o botão direito do mouse no item para abrir o menu de contexto e ative a opção <b>sincronizar plano de fundo</b><br />
O valor BPM atual será exibido no canto superior direito da janela principal.<br />
<img src='images/bpm_sync_img03.png' /><br />
<br />
<b>Observação:</b><br />
É recomendado utilizar vídeos com taxa FPS alta, pois a taxa FPS é um fator relevante na qualidade de execução quando a sincronização diminuir a velocidade de execução.<br />
Por exemplo, se o vídeo utilizado tem "apenas" 30 FPS e a sincronização realizada definir a velocidade do vídeo para 0.5, então o vídeo será executado, na prática, com 15 FPS, o que irá comprometer a qualidade da execução do vídeo.<br />
