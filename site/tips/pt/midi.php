<h1>Comunicação MIDI</h1>

<p>
    MIDI é um protocolo de comunicação entre instrumentos musicais eletrônicos e outros dispositivos eletrônicos (computador ou outro instrumento, por exemplo).<br />
    A funcionalidade MIDI no programa permite que ações sejam executadas no programa utilizando esse protocolo.<br />
    Na prática, é como uma determinada "nota" realizasse uma ação específica no programa.<br />
    Pode ser um teclado musical, por exemplo, ou um outro programa específico que controle e execute partituras e/ou multitracks.<br />
    Um uso prático seria uma banda que utiliza multitracks (tipo um playback que a banda utiliza para tocar junto).<br />
    O programa utilizado para executar as multitracks pode ser configurado com os comandos do programa durante a execução das tracks e, dessa forma, os slides serem passados no programa <b>automaticamente</b>, enquanto o "playback" é executado e a banda está tocando e/ou cantando.
</p>

