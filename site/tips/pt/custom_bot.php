<h1>Telegram Chatbot Personalizado</h1>

É possível utilizar as funcionalidades do Holyrics que utilizam um chatbot do Telegram com um chatbot próprio, personalizado com o nome e foto da sua igreja, por exemplo.
<br />
<hr />
<h3>Criar um bot</h3>
Acesse o chatbot <a href="https://t.me/BotFather" target="_blank">t.me/BotFather</a> para criar um bot do Telegram e editar as informações.
<br />
Para criar um bot, inicie a conversa com o comando <code>/newbot</code> e siga as instruções.
<br /> <br />
- Primeiro informe o nome do bot, o nome de sua preferência, com acentuação, espaço, etc.
<br />
Exemplo: <code>Holyrics Bot Personalizado</code>
<br /> <br />
- Depois informe um nome de usuário.
<br />
É permitido apenas letras de a-z A-Z, números e underscore.
<br />
Como regra, é necessário que o nome do bot termine com <code>Bot</code> ou <code>_bot</code>.
<br />
Exemplo: <code>HolyricsPersonalizadoBot</code> ou <code>HolyricsPersonalizado_bot</code>
<br /> <br />
- Após criar o bot, copie o token api <code>(exemplo: 5916........NxBPBMoj4U)</code> para configurar o chatbot personalizado no Holyrics.
