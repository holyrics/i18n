<h1>Projeção Panorâmica</h1>
<br />
<p>
    Projeção panorâmica é a projeção feita juntando dois ou mais projetores para formar uma "super tela larga".<br />
</p>
<p>
    Muitas pessoas acham que não é possível realizar esse tipo de projeção com o Holyrics porque o próprio programa não disponibiliza recurso de edge blending.<br/>
    Porém a junção das telas não precisa ser feita exclusivamente pelo software de projeção, pode ser feita com qualquer outro software de terceiro ou até mesmo com hardware específico.
</p>
<p>
    Por exemplo:<br />
    Através de software: <a href='https://fly.elise-ng.net/immersive-display-pro/' target='_blank'>Immersive Display Pro</a><br />
    Através de hardware: <a href='https://www.matrox.com/graphics/en/products/graphics_cards/m_series/powerdesk/edge_overlap/' target='_blank'>Matrox M-Series</a>
</p>
<p>
    É caro? Sim. Mas qualquer outro software de projeção que faça edge blending também é caro, e às vezes o plugin do programa de projeção que faz edge blending é mais caro do que o software externo que faz o mesmo.<br />
    E também utilizando este recurso através de software de terceiro ou hardware você tem a vantagem de poder utilizá-lo em qualquer programa.
</p>
<p>
    Através de software você precisará de múltiplas saídas de vídeo, por exemplo:<br />
    Uma projeção juntando dois projetores você precisaria, no mínimo, de três saídas: uma é sua própria tela e as outras duas para os projetores.<br />
    É importante levar em consideração que a placa de vídeo precisa ter suporte para juntar as duas ou mais saídas para os projetores.<br />
    Alguns modelos da nVidia possuem o sistema <b>Surround Display</b> (<a href='https://www.geforce.com/hardware/technology/surround/supported-gpus' target='_blank'>saiba mais</a>) e nas placas da AMD esse recurso é o <b>EyeFinity</b> (<a href='https://www.amd.com/en/technologies/eyefinity-professionals' target='_blank'>saiba mais</a>).<br />
    O sistema da placa de vídeo juntará as duas saídas de vídeo da projeção e criará uma "saída de vídeo virtual", que é a projeção panorâmica.<br />
    Tanto para o sistema operacional quanto para o Holyrics ou qualquer outro software, é como se o sistema tivesse apenas a tela larga da projeção panorâmica e não existissem as duas saídas de vídeo dos projetores.
</p>
<p>
    Através de hardware você não precisa se preocupar com as saídas de vídeo, pois o próprio dispositivo terá as saídas necessárias para a projeção e, assim como no caso do software, para o sistema operacional só existe a "tela larga" da projeção panorâmica.
</p>
<p>
    Com a projeção panorâmica configurada no seu sistema é só utilizar o Holyrics normalmente.<br />
    O programa vai exibir a projeção na "super tela larga" como qualquer outra tela.
</p>
<p>
    <b>Resumindo: Você pode fazer projeção panorâmica com Holyrics ou com qualquer outro software de projeção.</b>
</p>