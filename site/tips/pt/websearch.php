<h1>Pesquisa online - holyrics.com</h1>

<p>
    O programa Holyrics tem um banco de dados próprio para pesquisa de letras online.<br />
    (menu música, pesquisar online, holyrics.com)
</p>
<p>
    Nosso objetivo é disponibilizar letras com uma formatação já apropriada para projeção, pois por melhor que seja a edição da letra em outros sites como Letras ou Vagalume, na maioria das vezes o layout não é o ideal para projeção: as linhas são compridas e/ou o parágrafo tem muitas linhas, além de alguns erros de digitação.
</p>
<p>
    Outra vantagem também é que a grande maioria das letras está disponível com o campo <b>autor (compositor)</b> definido, para que você possa realizar a projeção em conformidade com a lei de direitos autorais sem perder tempo procurando qual é o autor de cada música exibida.<br />
    Saiba mais em: <a href='/tips/author.html'>holyrics.com/tips/author.html</a>
</p>
<p>
    Digital Millennium Copyright Act ("DMCA")<br />
