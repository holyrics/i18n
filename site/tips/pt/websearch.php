</p>

<p>
    Ao formatar uma letra, disponibilizarmos 4 tipos de formatação diferentes:<br />
    <br />
    • Padrão, máx 4 linhas;<br />
    • Padrão, máx 2 linhas;<br />
    • Widescreen, máx 2 linhas;<br />
    • Panorâmico, máx 2 linhas.
</p>
<p>
    Na pesquisa online pelo holyrics.com há um campo chamado "Formatação".<br />
    <img src='images/websearch_holyrics_img01.png' /><br />
    <br />
    Se o campo <b>formatação</b> estiver ativado ao selecionar uma música, significa que ela já foi formatada e está apropriada para exibição na projeção.<br />
    Provavelmente você não terá trabalho algum para editar a letra, basta apenas dar uma rápida conferida antes de salvar a música e já está pronta pra projeção.<br />
    Caso esteja "Indisponível", significa que a letra não foi editada e você precisa conferir e editá-la antes da utilização na projeção.<br />
</p>
