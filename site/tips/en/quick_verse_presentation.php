<h1>Quick Verse Presentation</h1>

When displaying a <b>Text</b> item or a <b>.pptx</b> file, if there is a Bible reference in the slide text, the verse can be displayed quickly without ending the current presentation.<br />
<br />
Right-click on the slide in the footer of the presentation and use the <b>Quick Presentation</b> option.<br />
<br />
<img src='images/quick_verse_presentation_img01.png' /><br />
<br />
Reference example: <br />
Gn 1.3<br />
Psalms 23<br />
John 3:16<br />
RM 12:1-2<br />
<br />
Slide text can contain multiple references.<br />
<br />
<b>Note:</b><br />
The name of the book and the abbreviation that will be identified will be according to the main version defined in the Bible window in the program.<br />
If any references are not being identified, check that the name of the book on the slide is in the same language as the default Bible in the Bible window.<br />
