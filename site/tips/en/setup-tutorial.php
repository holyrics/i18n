<h1>Installation Tutorial Mac</h1>
<br />
<p>
    Extract downloaded files (<b>Holyrics.zip</b>) to any folder on your computer.<br />
    Open the file <b>Holyrics.jar</b> to run the program.<br />
    If you have an error opening the file, you need to check/install Java 8 on the system, according to the tutorial below.<br />
    After installing Java, open the file <b>Holyrics.jar</b> to run the program.<br />
</p>
To install Java:<br />
<p style='padding-left:32px'>
    Open the terminal and run the commands to uninstall any version of Java currently installed on the computer:<br />
    <code>sudo rm -fr /Library/Internet\ Plug-Ins/JavaAppletPlugin.plugin</code><br />
    <code>sudo rm -fr /Library/PreferencesPanes/JavaControlPanel.prefPane</code><br />
    <code>sudo rm -fr ~/Library/Application\ Support/Java</code><br />
    <br />
    Then install Java 8 directly from the website <a href='https://java.com/en/download/manual.jsp' target='_blank'>java.com</a><br />
    Or download compatible Java<a href='/download/file/jre-8u211-macosx-x64.dmg'> (Download)</a><br />
</p>