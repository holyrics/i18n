<h1>Panoramic Projection</h1>
<br />
<p>
    Panoramic projection is the projection made by joining two or more projectors to form a "super widescreen".<br />
</p>
<p>
    Many people think that it is not possible to realize this type of projection with Holyrics because the program itself does not offer an edge blending feature.<br/>
    However the screen junction does not have to be exclusively made by the projection software, it can be done with any other third party software or even with specific hardware.
</p>
<p>
    For example:<br />
    Software: <a href='https://fly.elise-ng.net/immersive-display-pro/' target='_blank'>Immersive Display Pro</a><br />
    Hardware: <a href='https://www.matrox.com/graphics/en/products/graphics_cards/m_series/powerdesk/edge_overlap/' target='_blank'>Matrox M-Series</a>
</p>
<p>
    Is expensive? Yes. But any other projection software that does edge blending is also expensive, and sometimes the plugin of the projection program that does edge blending is more expensive than the external software that does the same.<br />
    And also using this feature through third party software or hardware you have the advantage of being able to use it in any program.
</p>
<p>
    Through software you will need multiple video outputs, for example:<br />
    A projection joining two projectors would require at least three outputs: one is your own screen and the other two projectors.<br />
    It is important to take into consideration that the video card must have support to join the two or more outputs to the projectors.<br />
    Some nVidia models have the <b>Surround Display</b> (<a href='https://www.geforce.com/hardware/technology/surround/supported-gpus' target='_blank'>know more</a>) and on AMD GPUs this feature is the <b>EyeFinity</b> (<a href='https://www.amd.com/en/technologies/eyefinity-professionals' target='_blank'>know more</a>).<br />
    The GPU will join the two video outputs of the projection and create a "virtual video output", which is the panoramic projection.<br />
    For both the operating system and Holyrics or any other software, it is as if the system had only the wide screen of the panoramic projection and did not exist the two video outputs of the projectors.
</p>
<p>
    Through hardware you do not have to worry about the video outputs, since the device itself will have the outputs necessary for projection and, just like in the case of the software, there is only the "wide screen" of the panoramic projection for the operating system.
</p>
<p>
    With the panoramic projection configured on your system just use the Holyrics normally.<br />
    The program will display the projection on the "super widescreen" just like any other screen.
</p>
<p>
    <b>In summary: You can do panoramic projection with Holyrics or any other projection software.</b>
</p>