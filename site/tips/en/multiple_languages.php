<h1>Song lyrics in multiple languages</h1>

In some situations it is necessary to display the lyrics of a song in two (or more) different languages.<br />
It is possible to do this in a practical way in the program, maintaining the independence of each slide, which makes configuration easier for the user.<br />
<br />
<ul>
    <li>
        In the lyrics editing window, access the <b>3 dots</b> icon, <b>Advanced Editor</b> option.<br />
        <img src='images/multiple_languages_img01.jpg' /><br />
        <br />
    </li>
    <li>
        Access the <b>Translation</b> option (icon: <img src="data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAABAAAAAQCAYAAAAf8/9hAAAAqklEQVR42mNgoBHQAeISIJ4NxPOBuAYqRhDwQDX8BuL/UPo7lA3Cq4FYBJ/m3UD8HIhjgPgzEPcDcTsQv4eKPQbiw7gMmQ/VrABV/BuqUATqihSo3GOoSzD8DNJwHmrQZait86H4NRBfh7LPQ72DEiYlaBpANp5G4h+HWoBsYA2yAaDQXo/E/w11MgwkQG2FgeVQg6hnAMVeoDgQKY5GqiQkipMyVTITyQAABEZgxYNbhZoAAAAASUVORK5CYII=" />);<br />
        <img src='images/multiple_languages_img02.jpg' /><br />
        <br />
    </li>
    <li>
        Create a translation and fill the corresponding lyric slides with the translated text.<br />
        <img src='images/multiple_languages_img03.jpg' /><br />
        <br />
    </li>
    <li>
        Check the result in the edit window and save the change.<br />
        <img src='images/multiple_languages_img04.jpg' /><br />
        <br />
    </li>
</ul>
<br />
That's it, that way you can easily add translations independently for each paragraph of the song's lyrics.<br />
And then you can set in the program how the song lyrics will be displayed on each screen.<br />
File menu, display settings.<br />
<img src='images/multiple_languages_img05.jpg' /><br />
<br />
There are several possibilities for use:<br />
Display only the original slide on one screen and display only the translated slide on another screen.<br />
<br />
Display the original and translated slide simultaneously on the same screen, with the text separated by a blank line.<br />
<pre>Original a
Original b
 
<i>Translated a
Translated b</i></pre>
<br />
Display the original and translated slide simultaneously on the same screen, with the text merged.<br />
<pre>Original a
<i>Translated a</i>
 
Original b
<i>Translated b</i></pre>
<br />
You can also define a different style/format for the text that is translated, adding a highlight between the different texts.<br />
