<h1>Holyrics - Script</h1>

It is possible to create scripts based on the JavaScript programming language that perform actions in the Holyrics program.<br />
<br />
These scripts can automate tasks and perform a list of actions at once.<br />
<br />
It is possible to create items to run with a mouse click, adding the items to the favorites bar, for example.<br />
<br />
And also for automatic execution in the program's <b>Triggers</b> functionality, where an action is automatically executed when another action is performed in the program, such as a slide change, for example.<br />
<br />
Scripts are created by the <b>Script</b> submenu available in the favorites bar and in the tools menu, several.<br />
Or using the <b>Holyrics - Script</b> receiver available in the receivers list of an <b>API Item</b> or the <b>Triggers</b> functionality.<br />
<br />
