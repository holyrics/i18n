<p>
    When formatting a letter, we provide 4 different types of formatting:<br />
    <br />
    • Standard, max 4 lines;<br />
    • Standard, max 2 lines;<br />
    • Widescreen, max 2 lines;<br />
    • Panoramic, max 2 lines.
</p>
<p>
    In the online search for holyrics.com there is a field called "Formatting".<br />
    <img src='images/websearch_holyrics_img01.png' /><br />
    <br />
    If the <b>formatting</b> field is activated when selecting a song, it means that it has already been formatted and is suitable for display in projection.<br />
    You probably won't have to do any work editing the lyrics, just give it a quick check before saving the song and it's ready for projection.<br />
    If it is "Unavailable", it means that the lyrics have not been edited and you need to check and edit them before using them in the projection.<br />
</p>
