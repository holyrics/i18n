<h1>Web Search - holyrics.com</h1>

<p>
    The Holyrics program has its own database for searching lyrics online.<br />
    (song menu, web search, holyrics.com)
</p>
<p>
    Our objective is to provide lyrics with a format that is already suitable for projection, because no matter how good the editing of the lyrics is on other sites such as Letras or Vagalume, most of the time the layout is not ideal for projection: the lines are long and/or the paragraph has many lines, as well as some typos.
</p>
<p>
    Another advantage is that the vast majority of lyrics are available with the <b>author (composer)</b> field defined, so you can carry out the projection in accordance with copyright law without wasting time looking for the right one. author of each song displayed.<br />
    Find out more at: <a href='/tips/author.html'>holyrics.com/tips/author.html</a>
</p>
