To open a page full screen on the projection screen, create a <b>URL</b> item in the playlist or favorites.<br />
<br />
When executing the item, check the option <b>Display on screen (Projection)</b> and click <b>OK</b>.<br />
<br />
The link will open in the browser in full screen on the projection screen.<br />
A small basic controls window will open, allowing you to execute some commands on the page without having to drag the mouse to the browser.<br />
<br />
<br />
Note: It is not considered a presentation, that is, the website will only be displayed on the public screen, it will not be displayed on the return screens.<br />
It is only available for Windows.<br />
It is necessary to have Chrome or Edge available on the system.<br />
<br />
