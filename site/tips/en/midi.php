<h1>MIDI Communication</h1>

<p>
    MIDI is a communication protocol between electronic musical instruments and other electronic devices (computer or other instrument, for example).<br />
    The MIDI functionality in the program allows actions to be performed in the program using this protocol.<br />
    In practice, it's like a certain "note" performing a specific action in the program.<br />
    It could be a musical keyboard, for example, or another specific program that controls and executes scores and/or multitracks.<br />
    A practical use would be a band that uses multitracks (like a playback that the band uses to play together).<br />
    The program used to execute the multitracks can be configured with the program commands during the execution of the tracks and, in this way, the slides are passed through the program <b>automatically</b>, while the "playback" is executed and the band is playing and/or singing.
</p>

