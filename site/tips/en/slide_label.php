<h1>Slide Description</h1>

The <b>Slide Description</b> feature works as a facilitator for slide identification in the presentation footer.<br>
<br />
The paragraph/slide description is defined:

<ul>
    <li>
        in the song editing window, after two ##, inside parentheses;<br />
        <img src='images/slide_label_img02.png' />
        <br />&nbsp;
    </li>
    <li>
        in the song editing window, in the slide preview;<br />
        <img src='images/slide_label_img03.png' />
        <br />&nbsp;
    </li>
    <li>
        in the main window, right-click.<br />
        <img src='images/slide_label_img01.png' />
        <br />&nbsp;
    </li>
</ul>

<br>
<br>

Can be used for:
<ul>  
    <li>set shortcuts at <b>go to</b> function, e.g: <b><i>X</i></b> letter go to slide <b><i>X</i></b>;</li>
    <li>set a default Theme, e.g: all <b><i>X</i></b> slides will get the <b><i>X</i></b> theme;</li>
    <li>custom formatting, e.g: all <b><i>X</i></b> slides with italic font</b>.</li>
</ul>
<br>

<img src='images/slide_label_img04.png' />
<br />
<br />
That way, you can type the shortcut and then <i><b>ENTER</b></i> in the "go to..." text box in the presentation footer that will change to the music slide as described.<br />
