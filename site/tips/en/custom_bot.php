<h1>Custom Telegram Chatbot</h1>

It is possible to use Holyrics features that use a Telegram chatbot with your own chatbot, personalized with the name and photo of your church, for example.
<br />
<hr />
<h3>Create a bot</h3>
Go to the chatbot <a href="https://t.me/BotFather" target="_blank">t.me/BotFather</a> to create a Telegram bot and edit the information.
<br />
To create a bot, start the conversation with the command <code>/newbot</code> and follow the instructions.
<br /> <br />
- First enter the name of the bot, the name of your preference, with accents, space, etc.
<br />
Example: <code>Holyrics Custom Bot</code>
<br /> <br />
- Then enter a username.
<br />
Only letters a-z A-Z, numbers and underscores are allowed.
<br />
As a rule, the bot name must end with <code>Bot</code> or <code>_bot</code>.
<br />
Example: <code>HolyricsCustomBot</code> or <code>HolyricsCustom_bot</code>
<br /> <br />
- After creating the bot, copy the api token <code>(example: 5916........NxBPBMoj4U)</code> to configure the custom chatbot in Holyrics.
