<b>[Wallpaper] Definir um papel de parede para cada culto (menu arquivo, configurações)</b>
Ao definir um papel de parede para um culto, o programa utilizará automaticamente o papel de parede do culto selecionado na lista de reprodução da janela principal.
Caso não haja papel de parede definido para o culto selecionado, ou se a lista de reprodução <b>temporária</b> estiver selecionada, será utilizado o papel de parede padrão.
Obs.: O papel de parede exibido não é de acordo com o <b>horário do culto</b>, mas sim de acordo com o culto selecionado na <b>lista de reprodução</b> da janela principal.
Obs.: Também é possível definir um papel de parede para cada culto na opção <b>wallpaper</b> da barra de favoritos.


<b>[Escala] Filtrar integrantes para cada função</b>
Ao editar a escala no modo função, a lista de usuários que aparecem para cada função é baseada no grupo definido para o integrante e para a função, por exemplo:
Se a função <b>violão</b> está definida como grupo <b>instrumento</b>, todos os integrantes do grupo <b>instrumento</b> aparecem na lista para serem escalados na função <b>violão</b>.
Agora é possível definir quais usuários do grupo <b>instrumento</b> aparecerão na lista para serem escalados na função <b>violão</b>.


<b>[Mídia] Edição básica de áudio, vídeo e imagem</b>
Algumas ferramentas de edição estarão disponíveis ao clicar com o botão direito do mouse em um item de mídia nas abas da janela principal.
Também estará disponível no menu ferramentas, diversos.


<b>[Vídeo] Solução alternativa para executar vídeos com rotação não identificados pelo programa</b>
Alguns vídeos (geralmente gravados pelo celular) não são executados corretamente no programa, pois o VLC Player não identifica a configuração de rotação do vídeo.
Foi implementada uma solução alternativa para evitar esse imprevisto, pois em muitos casos isso é reparado apenas no momento do uso.
Caso o programa identifique que o vídeo tem uma configuração de rotação definida nos "metadados" do arquivo, o vídeo será executado <b>externamente</b> pelo VLC Player diretamente no sistema operacional.


<b>Opção para adicionar um vídeo como plano de fundo do slide sem utilizar a aba "meus vídeos"</b>
Na janela de edição de música, para definir um vídeo como plano de fundo para um slide é necessário adicioná-lo primeiro na aba <b>Meus Vídeos</b> na lista de planos de fundo dos Temas.
Agora é possível definir um vídeo específico como plano de fundo de um slide sem adicioná-lo na aba <b>Meus Vídeos</b>.
Basta arrastar o arquivo de vídeo da pasta do sistema operacional (Windows Explorer, por exemplo), para cima do slide.
Obs.: Recomendado apenas se você deseja utilizar um vídeo específico para determinado slide.
Se o vídeo será reutilizado em outros slides de outras músicas, ainda é recomendado adicioná-lo na aba <b>Meus Vídeos</b>.