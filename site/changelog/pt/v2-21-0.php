    <dd>• [Favoritos] Opção para definir um ícone personalizado para um item pasta;</dd>
    <dd>• [Seleção de Arquivo] Atalho para a pasta <i>Downloads</i> na janela de seleção de arquivo;</dd>
    <dd>• [Rodapé de Apresentação - Ir Para] Opção para executar a mudança de slide sem pressionar <i>Enter</i> (botão direito do mouse na caixa de texto);</dd>
    <dd>• [Múltipla Escolha] Executar áudios diferentes dependendo do índice da pergunta;</dd>
    <dd>• [Relatório de Músicas Tocadas] Opção para mesclar histórico de itens duplicados, por exemplo, duas letras de músicas "iguais" de cantores diferentes;</dd>
    <dd>• [Apresentação Automática] Opção para editar título, autor, artista e informações extras da música.</dd>
