  <dt>Novidades</dt>
    <dd>• [Plano de Fundo - Vídeo] Opção para exibir plano de fundo de vídeo em uma tela adicional, além da tela público (menu arquivo, configurações, avançado);</dd>
    <dd>• [Teclas de Atalho] Opção para editar/criar as teclas de atalho para ações padrões do programa (menu ajuda, teclas de atalho);</dd>
    <dd>• [Evento - Papel de Parede] Opção para definir um papel de parede específico para um evento;</dd>
    <dd>• [Visão do Palco] Opção para exibir a próxima imagem na tela de retorno ao apresentar uma lista de imagens. Compatível também com apresentação ppt/pptx;</dd>
    <dd>• [Sorteio] Possibilidade de sortear múltiplos itens simultaneamente;</dd>
    <dd>• [Playlist] Calcular e exibir o horário da programação de cada item da lista de reprodução baseado na hora de início do evento e a duração de cada item;</dd>
    <dd>• [Painel de Comunicação] Configuração <i>"cor amarela a partir de"</i> substituída por <i>"cor de atenção a partir de"</i>;</dd>
    <dd>• [Painel de Comunicação] Opção para alterar a <i>cor de atenção</i> e a cor de outros estados na aba configurações;</dd>
    <dd>• [Áudio / Vídeo] Opção para definir configurações para execução da mídia nos favoritos ou playlist (botão direito do mouse no item). Por exemplo, volume, segundo inicial, etc.;</dd>
    <dd>• [Letra de Música] Opção via API para abrir a janela de criação de letra pré-preenchida (<a href='/go.php?action=api_popup_create_song&lang=pt' target='_blank'>Documentação</a>);</dd>
    <dd>• [Contagem Regressiva - Público] Opção para personalizar a fonte da contagem regressiva;</dd>
    <dd>• [Painel de Comunicação] Editar o tema utilizado para contagem regressiva;</dd>
    <dd>• [Telegram - Pergunta, Enquete, Sorteio] Opção para que seja enviada notificações a grupos e/ou canais do Telegram informando a disponibilidade de um novo evento;</dd>
    <dd>• [Chat] Opção para sincronizar o Chat do programa com um grupo do Telegram (botão configurações da janela do Chat);</dd>
    <dd>• [JSLib] Novos métodos JSLib (<a href='/go.php?action=jslib_doc&lang=pt' target='_blank'>Documentação</a>).</dd>
