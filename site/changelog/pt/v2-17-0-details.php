<b>Sincronização em nuvem via Google Drive</b>
Você pode fazer login em uma conta Google e utilizar o espaço de armazenamento do Google Drive para fazer sincronização de todos os dados e arquivos do programa entre diferentes computadores.
Menu compartilhar > Sincronização em nuvem

Os dados e arquivos serão adicionados, atualizados e apagados entre todos os computadores conectados na mesma conta.
Obs.: Não há controle de usuário e/ou senha para a sincronização em nuvem.
Todos os computadores sincronizados na mesma conta poderão adicionar, alterar ou remover arquivos do programa sincronizados na nuvem.


<b>Backup automático via Google Drive</b>
Você pode fazer login em uma conta Google e utilizar o espaço de armazenamento do Google Drive para fazer backup automático de todos os dados e arquivos do programa.
Menu Ferramentas > Backup > Google Drive > Ativar backup automático

Ao ativar o backup, o programa manterá um backup online sempre atualizado a cada alteração no programa.
Você pode restaurar um backup quando necessário:
Menu Ferramentas > Backup > Google Drive > Restaurar


<b>(Visão do Palco) Exibir todos os slides da música</b>
Opção para exibir na tela de retorno o texto inteiro da música sendo apresentada.
Também é possível definir uma formatação diferente para o slide atual em exibição (ou outros slides).
Dessa forma, mesmo com o texto completo sendo exibido na tela de retorno, o trecho do slide atual em exibição ficará em destaque.


<b>[PowerPoint] Apresentação rápida de versículos</b>
Opção para que seja possível exibir um versículo da Bíblia na projeção sem que seja necessário encerrar a apresentação PowerPoint em exibição pelo Holyrics.
Isso poderá ser feito pelo próprio rodapé de apresentação da janela principal, clicando no botão <b>'exibir versículo'</b> (próximo aos botões <b>F8, F9, ...</b>)

Para facilitar, o programa tenta identificar automaticamente alguma referência dentro de cada slide.
Dessa forma, no rodapé de apresentação, clicando com o botão direito em cima de um slide que tenha alguma referência bíblica, aparecerá um atalho para exibir o determinado versículo.
Por exemplo: Se no slide estiver escrito em algum lugar <b><i>Jo 3:16</i></b> ou <b><i>Romanos 12.2</i></b>, então você pode clicar com o botão direito do mouse em cima desse slide no rodapé de apresentação que vai aparecer a opção "Apresentação Automática" e o versículo na lista de itens para exibição.