<b>Traduzir (manualmente) slides de uma música ou texto</b>
É possível criar traduções para cada slide de uma música de forma simples.
Após traduzido, exibir o texto original na projeção principal e o texto traduzido em <b>outra tela</b> ou múltiplas traduções na mesma tela.
Para utilizar este recurso é necessário acessar o editor avançado na janela de edição de música/texto.