<b>[Telegram] Funcionalidades 'sorteio', 'enquete' e 'perguntas' através do Telegram</b>
É possível criar esses eventos no programa para que as pessoas possam participar utilizando o próprio Telegram.
A participação é feita utilizando o chat <a href='https://t.me/HolyricsBot' target='_blank'>@HolyricsBot</a>.
Por exemplo: crie uma enquete pelo programa e informe aos participantes o nome da enquete.
As pessoas poderão votar acessando o chat e você poderá exibir o resultado em tempo real na projeção.


<b>[Rodapé de Apresentação] Funcionalidade de alterar Tema com opção para alterar somente música atual ao executar lista de músicas</b>
No rodapé de apresentação, ao executar uma lista de músicas e alterar o tema ou plano de fundo dos slides durante a apresentação, todos os slides são alterados por padrão.
Essa opção permite alterar o funcionamento padrão para alterar somente os slides da música em exibição naquele momento.


<b>[MIDI] Opção para exibir o rodapé de apresentação da janela principal ao iniciar uma apresentação</b>
Ao iniciar uma apresentação de forma remota (como comandos via MIDI) o rodapé de apresentação na janela principal do programa não é exibido.
Foi adicionada uma opção nas configurações da janela MIDI para que o rodapé seja exibido ao executar uma música e ser controlado pelo usuário, se necessário.